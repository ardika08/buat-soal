drop policy if exists "Users can read own profile" on public.profiles;
create policy "Users can read own profile"
on public.profiles for select
to authenticated
using (auth_user_id = auth.uid());

drop policy if exists "Users can update own profile" on public.profiles;
create policy "Users can update own profile"
on public.profiles for update
to authenticated
using (auth_user_id = auth.uid())
with check (auth_user_id = auth.uid());

drop policy if exists "Users can read own transactions" on public.credit_transactions;
create policy "Users can read own transactions"
on public.credit_transactions for select
to authenticated
using (user_id in (select id from public.profiles where auth_user_id = auth.uid()));

drop policy if exists "Users can read own exams" on public.exam_sessions;
create policy "Users can read own exams"
on public.exam_sessions for select
to authenticated
using (user_id in (select id from public.profiles where auth_user_id = auth.uid()));

drop policy if exists "Users can delete own exams" on public.exam_sessions;
create policy "Users can delete own exams"
on public.exam_sessions for delete
to authenticated
using (user_id in (select id from public.profiles where auth_user_id = auth.uid()));

drop policy if exists "Users can read own questions" on public.questions;
create policy "Users can read own questions"
on public.questions for select
to authenticated
using (
  exam_session_id in (
    select exam_sessions.id
    from public.exam_sessions
    join public.profiles on profiles.id = exam_sessions.user_id
    where profiles.auth_user_id = auth.uid()
  )
);

drop policy if exists "Users can update own questions" on public.questions;
create policy "Users can update own questions"
on public.questions for update
to authenticated
using (
  exam_session_id in (
    select exam_sessions.id
    from public.exam_sessions
    join public.profiles on profiles.id = exam_sessions.user_id
    where profiles.auth_user_id = auth.uid()
  )
)
with check (
  exam_session_id in (
    select exam_sessions.id
    from public.exam_sessions
    join public.profiles on profiles.id = exam_sessions.user_id
    where profiles.auth_user_id = auth.uid()
  )
);

